package lemoon.can.milkyway.common.enums;

/**
 * 帖子内容类型
 * @author lemoon
 * @since 2025/6/3
 */
public enum PostContentType {
    /**
     * 文字
     */
    TEXT,
    /**
     * 图片
     */
    IMAGE,
    /**
     * 视频
     */
    VIDEO,
    /**
     * 链接
     */
    LINKED,
    /**
     * 文字+图片
     */
    TEXT_IMAGE,
    /**
     * 文字+视频
     */
    TEXT_VIDEO,
    /**
     * 文字+链接
     */
    TEXT_LINKED,
    ;
}
